package com.kumaduma.epicseveninfo.Model.Catalyst;

public class Locations {
    private String node;
    private String name;
    private String mobcount;

    public Locations(){}

    public String getNode() {
        return node;
    }

    public void setNode(String node) {
        this.node = node;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobcount() {
        return mobcount;
    }

    public void setMobcount(String mobcount) {
        this.mobcount = mobcount;
    }
}
